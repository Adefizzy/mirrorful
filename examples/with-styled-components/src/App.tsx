import { useState } from 'react'
import { ThemeProvider } from 'styled-components'

import {
  StyledPrimaryButton,
  StyledContainter,
  StyledSecondaryButton,
} from './styles'
import { GlobalStyles } from './globalTheme'

import useTheme from './useTheme'

function App() {
  const { theme, toggleTheme } = useTheme()
  const [count, setCount] = useState(0)

  return (
    <ThemeProvider theme={theme}>
      <GlobalStyles />

      <div>
        <h1>Mirrorful + Styled-Components + Vite + React</h1>
        <StyledPrimaryButton onClick={() => setCount((count) => count + 1)}>
          count is {count}
        </StyledPrimaryButton>
        <StyledSecondaryButton onClick={toggleTheme}>
          toggle theme
        </StyledSecondaryButton>
        <p>
          This button is styled with Styled-Components which uses the theme
          generated by Mirrorful
        </p>
      </div>
    </ThemeProvider>
  )
}

export default App
